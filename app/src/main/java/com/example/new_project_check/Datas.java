package com.example.new_project_check;

public class Datas {
    private String disesae;
    private String treatment;
    private String DocName;
    private String hopName;
    private String date1;
    private Integer ageee;
    private String Imageid;

    public Datas() {
    }

    public String getDisesae() {
        return disesae;
    }

    public void setDisesae(String disesae) {
        this.disesae = disesae;
    }

    public String getTreatment() {
        return treatment;
    }

    public void setTreatment(String treatment) {
        this.treatment = treatment;
    }

    public String getDocName() {
        return DocName;
    }

    public void setDocName(String docName) {
        DocName = docName;
    }

    public String getHopName() {
        return hopName;
    }

    public void setHopName(String hopName) {
        this.hopName = hopName;
    }

    public String getDate1() {
        return date1;
    }

    public void setDate1(String date1) {
        this.date1 = date1;
    }

    public Integer getAgeee() {
        return ageee;
    }

    public void setAgeee(Integer ageee) {
        this.ageee = ageee;
    }

    public String getImageid() {
        return Imageid;
    }

    public void setImageid(String imageid) {
        Imageid = imageid;
    }
}

